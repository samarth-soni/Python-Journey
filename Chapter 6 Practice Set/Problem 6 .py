Marks = int(input("Enter the marks of the Student : "))

if(Marks<=100 and Marks>=90):
    print("Ex Grade")

elif(Marks<=90 and Marks>=80):
    print()

elif(Marks<=90 and Marks>=80):
    print("A Grade")

elif(Marks<=80 and Marks>=70):
    print("B grade")

elif(Marks<=70 and Marks>=60):
    print("C grade")

elif(Marks<=60 and Marks>=50):
    print("D grade")

elif(Marks<50):
    print("F grade")    